package com.Discord_Bot_Tool.bot;

public class BotInfo {
    private final String Token = ""; // 봇 토큰
    private final String ChannelID = ""; // 채널 ID
    private final String RoleID = ""; // 역할 ID

    public String getToken() {
        return Token;
    }

    public String getChannelID() {
        return ChannelID;
    }

    public String getRoleID() {
        return RoleID;
    }
}